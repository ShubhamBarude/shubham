package StarPattern;

public class StringPatIncreDec {
    public static void main(String[] args) {
        int a = 5;
        for (int m = 1; m <= a; m++) {
            for (int n = 1; n <= m; n++) {
                System.out.print("* ");
            }
            System.out.println();
        }
        for (int s = 1; s <= a; s++) {
            for (int d = s; d < a; d++) {
                System.out.print("* ");
            }
            System.out.println();
        }
       // {

        }

    }
