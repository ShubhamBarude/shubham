public class ThreeArrayAndPrintUnique
{
    public static void main(String[] args) {
        int i;
        int j;
        int arr[]={1,2,3,2,3};
        for( i=0;i<arr.length;i++){
            for( j=0;j<arr.length;j++);
            {
                if(arr[i]==arr[j]){
                    System.out.println(arr[i]);
                }


            }

        }
    }
}
